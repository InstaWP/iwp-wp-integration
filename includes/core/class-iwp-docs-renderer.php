    /**
     * Render inline JS for sidebar navigation and AJAX doc loading
     */
    private function render_docs_script() {
        ?>
        <script>
        jQuery(document).ready(function($) {
            var $sidebar = $('.iwp-docs-sidebar');
            var $content = $('#iwp-docs-content');

            function loadDoc(slug, pushState) {
                $sidebar.find('li').removeClass('active');
                $sidebar.find('a[data-doc="' + slug + '"]').closest('li').addClass('active');

                $content.css('opacity', '0.5');

                $.post(ajaxurl, {
                    action: 'iwp_load_doc',
                    doc: slug,
                    nonce: '<?php echo esc_js(wp_create_nonce('iwp_admin_nonce')); ?>'
                }, function(response) {
                    if (response.success) {
                        $content.html(response.data.html);
                        $content.scrollTop(0);
                        // Rebind internal links
                        bindDocLinks();
                    }
                }).always(function() {
                    $content.css('opacity', '1');
                });
            }

            function bindDocLinks() {
                $content.find('a[data-doc]').off('click').on('click', function(e) {
                    e.preventDefault();
                    loadDoc($(this).data('doc'), true);
                });
            }

            // Sidebar navigation
            $sidebar.on('click', 'a[data-doc]', function(e) {
                e.preventDefault();
                loadDoc($(this).data('doc'), true);
            });

            // Bind initial content links
            bindDocLinks();
        });
        </script>
        <?php
    }
