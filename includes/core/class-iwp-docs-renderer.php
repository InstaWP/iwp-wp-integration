    private $parsedown;

    /**
     * Constructor
     */
    public function __construct() {
        if (!class_exists('Parsedown')) {
            require_once IWP_PLUGIN_PATH . 'includes/core/lib/Parsedown.php';
        }
        $this->parsedown = new Parsedown();
        $this->parsedown->setSafeMode(true);
    }

    /**
     * Render the full docs viewer (sidebar + content)
     *
     * @param string $active_slug The currently active doc slug
     */
    public function render($active_slug = 'index') {
        // Validate slug
        if (!array_key_exists($active_slug, $this->docs)) {
            $active_slug = 'index';
        }

        $content_html = $this->get_doc_html($active_slug);

        $this->render_docs_css();
        ?>
        <div class="iwp-docs-viewer">
            <nav class="iwp-docs-sidebar">
                <ul>
                    <?php foreach ($this->docs as $slug => $title) : ?>
                        <li class="<?php echo $slug === $active_slug ? 'active' : ''; ?>">
                            <a href="#" data-doc="<?php echo esc_attr($slug); ?>">
                                <?php echo esc_html($title); ?>
                            </a>
                        </li>
                    <?php endforeach; ?>
                </ul>
            </nav>
            <div class="iwp-docs-content" id="iwp-docs-content">
                <?php echo $content_html; // Already sanitized in get_doc_html ?>
            </div>
        </div>
        <?php
        $this->render_docs_script();
    }

    /**
     * Get parsed HTML for a given doc slug
     *
     * @param string $slug Doc slug
     * @return string Sanitized HTML
     */
    private function get_doc_html($slug) {
        $file_path = IWP_PLUGIN_PATH . 'docs/' . $slug . '.md';

        if (!file_exists($file_path)) {
            return '<p>' . esc_html__('Documentation file not found.', 'iwp-wp-integration') . '</p>';
        }

        $markdown = file_get_contents($file_path);
        if ($markdown === false) {
            return '<p>' . esc_html__('Could not read documentation file.', 'iwp-wp-integration') . '</p>';
        }

        // Rewrite internal markdown links to JS-friendly links
        // e.g. [Settings](settings.md) → <a href="#" data-doc="settings">Settings</a>
        $markdown = preg_replace_callback(
            '/\[([^\]]+)\]\(([a-z0-9-]+)\.md\)/',
            function ($matches) {
                return '[' . $matches[1] . '](#doc-' . $matches[2] . ')';
            },
            $markdown
        );

        $html = $this->parsedown->text($markdown);

        // Rewrite the anchor links generated by Parsedown
        $html = preg_replace(
            '/href="#doc-([a-z0-9-]+)"/',
            'href="#" data-doc="$1" class="iwp-doc-link"',
            $html
        );

        return wp_kses_post($html);
    }

    /**
     * AJAX handler for loading docs
     */
    public static function ajax_load_doc() {
        check_ajax_referer('iwp_admin_nonce', 'nonce');

        if (!current_user_can('manage_options')) {
            wp_send_json_error('Insufficient permissions');
        }

        $slug = isset($_POST['doc']) ? sanitize_key($_POST['doc']) : 'index';
        $renderer = new self();
        $html = $renderer->get_doc_html($slug);

        wp_send_json_success(array('html' => $html));
    }

